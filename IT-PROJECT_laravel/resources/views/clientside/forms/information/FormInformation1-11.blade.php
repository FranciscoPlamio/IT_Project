            <!-- Description of authorities -->
            <div>
                <div class="bg-gray-50 rounded-lg p-4 text-gray-700 space-y-2">
                    <p>
                        A <span class="font-semibold">Permit to Purchase/Possess</span> is a written authority issued by
                        the Commission to an individual, private and government entities authorizing the holder thereof
                        to purchase/acquire and/or possess/own a radio transceiver.
                    </p>
                    <p>
                        A <span class="font-semibold">Fixed Aeronautical Station License</span> is a written authority
                        issued by the Commission to an individual, private and government entities authorizing the
                        holder thereof to operate an aeronautical fixed station in the Aeronautical Fixed Service.
                    </p>
                    <p>
                        An <span class="font-semibold">Aircraft Station License</span> is a written authority issued by
                        the Commission to an individual or private or government entities authorizing the holder thereof
                        to operate a mobile station installed onboard any type of aircraft.
                    </p>
                    <p>
                        The <span class="font-semibold">renewal</span> of Fixed Aeronautical Station License or Aircraft
                        Station License is required for the continuous operation of an existing radio station.
                    </p>
                    <p>
                        The <span class="font-semibold">modification</span> of Fixed Aeronautical Station License or
                        Aircraft Station License is required for changes in the particulars indicated in the License.
                    </p>
                </div>
            </div>

