            <!-- Description -->
            <div>
                <div class="bg-gray-50 rounded-lg p-4 text-gray-700 space-y-2">
                    <p>
                        An <span class="font-semibold">Accreditation for Radio Communications Equipment (RCE) Dealer or
                            RCE Manufacturer or RCE Service Center</span> is a written authority issued by the
                        Commission to a person, firm, company, association or corporation authorizing the holder
                        thereof to engage in the acquisition and purchase/sale, as a manufacturer, or servicing and
                        maintenance of radio communications equipment.
                    </p>
                    <p>
                        An <span class="font-semibold">Accreditation for Customer Premises Equipment Supplier</span> is
                        a written authority issued by the Commission to a person, firm, company, association or
                        corporation authorizing the holder thereof to engage in the acquisition, servicing, maintenance,
                        purchase or sale of equipment located in the premises of a customer which is not part of, but
                        connected to the system or network of a public telecommunications entity.
                    </p>
                    <p>
                        An <span class="font-semibold">Accreditation for Mobile Phone Dealer or Mobile Phone
                            Retailer/Reseller or Mobile Phone Service Center</span> is a written authority issued by the
                        Commission to a person, firm, company, association or corporation authorizing the holder
                        thereof to engage in the acquisition and purchase/sale, or servicing and maintenance of mobile
                        phones.
                    </p>
                </div>
            </div>

