            <!-- Description of authorities -->
            <div>
                <div class="bg-gray-50 rounded-lg p-4 text-gray-700 space-y-2">
                    <p>
                        The <span class="font-semibold">Ship Station License</span> is a written authority issued by the
                        Commission to an individual, private and government entities authorizing the holder thereof to
                        operate ship radio station (radio and navigation equipment) in the Maritime Mobile Service.
                    </p>
                    <p>
                        The <span class="font-semibold">Ship Earth Station License</span> is a written authority issued
                        by the Commission to an individual, private and government entities authorizing the holder
                        thereof to operate ship earth radio station (radio and navigation equipment) in the Maritime
                        Mobile Service.
                    </p>
                    <p>
                        The <span class="font-semibold">renewal</span> of a Ship Station License or Ship Earth Station
                        License is required for the continuous operation of an existing radio station.
                    </p>
                    <p>
                        A <span class="font-semibold">Private Coastal Station License</span> is a written authority
                        issued by the Commission to an individual, private and government entities authorizing the
                        holder thereof to operate a radio station in the Maritime Service.
                    </p>
                    <p>
                        The <span class="font-semibold">renewal</span> of a Private Coastal Station License is required
                        for the continuous operation of a private coastal station.
                    </p>
                    <p>
                        The <span class="font-semibold">modification</span> of a Ship Station License, Ship Earth
                        Station License, or Private Coastal Radio Station License is required for changes in the
                        particulars indicated in the license.
                    </p>
                    <p>
                        A <span class="font-semibold">Permit to Sell</span> is a written authority issued by the
                        Commission authorizing a person, company, association, or corporation to sell radio
                        communications equipment to a holder of a Permit to Purchase.
                    </p>
                    <p>
                        A <span class="font-semibold">Permit to Transfer</span> is a written authority issued by the
                        Commission authorizing the holder to transfer ownership of radio communications equipment to
                        another person or entity.
                    </p>
                </div>
            </div>

