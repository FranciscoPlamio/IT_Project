            <!-- Description of authorities -->
            <div>
                <div class="bg-gray-50 rounded-lg p-4 text-gray-700 space-y-2">
                    <p>
                        A <span class="font-semibold">Permit to Purchase/Possess</span> is a written authority issued by
                        the Commission to a public telecommunications entity (PTEs) authorizing the holder thereof to
                        purchase/acquire and/or possess/own a radio transceiver.
                    </p>
                    <p>
                        A <span class="font-semibold">Public Coastal Station License</span> is a written authority
                        issued by the Commission to a public telecommunications entity (PTEs) authorizing the holder
                        thereof to operate a public coastal station in the Maritime Service.
                    </p>
                    <p>
                        The <span class="font-semibold">modification</span> of a Public Coastal Station License is
                        required for changes in the particulars indicated in the License.
                    </p>
                    <p>
                        A <span class="font-semibold">Permit to Sell</span> is a written authority issued by the
                        Commission authorizing a person, company, association, or corporation to sell radio
                        communications equipment to a holder of a Permit to Purchase.
                    </p>
                    <p>
                        A <span class="font-semibold">Permit to Transfer</span> is a written authority issued by the
                        Commission authorizing the holder to transfer ownership of radio communications equipment to
                        another person or entity.
                    </p>
                </div>
            </div>

